package day05_concatenation_operatorler;

public class C01_Concatenation {
	public static void main(String[] args) {

		System.out.println("25" + "34");

		System.out.println("Java" + 2 + 7);
		System.out.println("Java" + (2 + 7));
		System.out.println("Java" + 2 * 7);
		System.out.println(2 + 7 + "Java");


	}
}
